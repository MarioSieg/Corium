version_lock_file = "Tools/SDKVersionLock.txt"

class SDKVersion:
    def __init__(self):
        self.major = 0
        self.minor = 0

    def __init__(self, major: int, minor: int):
        self.major = major
        self.minor = minor

    def __str__(self) -> str:
        return f"v.{self.major}.{self.minor}"

    def set(self, major: int, minor: int):
        self.major = major
        self.minor = minor

    def increment(self):
        self.minor += 1
        if self.minor >= 10:
            self.minor = 0
            self.major += 1

def set_version(ver: SDKVersion):
    with open(version_lock_file, "wt") as f:
        f.write(f"{ver.major}.{ver.minor}")

def get_version() -> SDKVersion:
    try:
        with open(version_lock_file, "rt") as f:
            version_str = f.read()
            subver = version_str.split(".")
            major = int(subver[0])
            minor = int(subver[1])
            return SDKVersion(major, minor)
    except:
        ver = SDKVersion(0, 0)
        set_version(ver)
        return ver

def get_os_name() -> str:
    osname = platform.system()
    return "MacOS" if osname == "Darwin" else osname

def is_admin() -> bool:
    if get_os_name() == "Windows":
        try:
            return ctypes.windll.shell32.IsUserAnAdmin()
        except:
            return False
    else:
        return os.geteuid() == 0

def generate_sdk_info(sdk_root: str):
    sdk_info_file = sdk_root + "SDK.txt"
    time = datetime.datetime.now().strftime("%m/%d/%Y %H:%M:%S")
    content = ""
    content += f"DATE={time}\n"
    content += f"SDK_VERSION={get_version()}\n"
    content += f"CORIUM_HOME={os.getcwd()}\n"
    with open(sdk_info_file, "wt") as f:
        f.write(content)

def create_archieve() -> str:
    file_name = f"CoriumSDK_{os_build_name}_{arch}"
    if not os.path.isdir(release_dir):
        mkdir(release_dir)
    src_dir = f"{release_dir}/{file_name}"
    if os.path.isfile(src_dir):
        os.remove(src_dir)
    shutil.make_archive(src_dir, "zip", out_dir)
    return file_name

def is_shell_tool_installed(cmd: str) -> bool:
    return shutil.which(cmd) is not None

def exec(cmd: str, use_pipe = False):
    if use_pipe:
        pipe = os.popen(cmd)
        print(pipe.read())
    else:
        splitted: list[str] = shlex.split(cmd)
        print("Executing command: " + str(splitted))
        p = subprocess.Popen(splitted, stdout=subprocess.PIPE, stderr=subprocess.PIPE, bufsize=2)
        for line in iter(p.stdout.readline, b''):
            print(str(line).replace("\n\'", "").replace("b'", ""))
        p.stdout.close()
        p.stderr.close()
        p.wait()

def mkdir(path: str):
    if os.path.isdir(path):
        return
    try:
        print("Creating dir: " + path)
        os.makedirs(path, exist_ok=True)
    except Exception as e:
        print("Failed to create dir: " + path)
        print("Because " + str(e))
        print("Exiting...")
        exit(-1)

def mk_exe_name(raw: str) -> str:
    osname = platform.system()
    return raw + ".exe" if osname == "Windows" else raw

def mk_symlink(src: str, dst: str):
    print(f"Creating symlink from {src} to {dst}")
    os.symlink(src, dst)

def first_dir_name(path: str) -> str:
    dirs = [x[0] for x in os.walk(path)]
    if len(dirs) == 0:
        print("Expected at least one directory here: " + path)
        exit(-1)
    return os.path.basename(dirs[1])

