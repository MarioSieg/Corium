import os
import os.path
import platform
import shutil
import shlex
import subprocess
import multiprocessing

os.chdir("../")

def is_shell_tool_installed(cmd: str) -> bool:
    return shutil.which(cmd) is not None

def exec(cmd: str, use_pipe = False):
    if use_pipe:
        pipe = os.popen(cmd)
        print(pipe.read())
    else:
        splitted: list[str] = shlex.split(cmd)
        print("Executing command: " + str(splitted))
        p = subprocess.Popen(splitted, stdout=subprocess.PIPE, stderr=subprocess.PIPE, bufsize=2)
        for line in iter(p.stdout.readline, b''):
            print(str(line).replace("\n\'", "").replace("b'", ""))
        p.stdout.close()
        p.wait()

def mkdir(path: str):
    if os.path.isdir(path):
        return
    try:
        print("Creating dir: " + path)
        os.mkdir(path)
    except Exception as e:
        print("Failed to create dir: " + path)
        print("Exiting...")
        exit(-1)

def mk_exe_name(raw: str) -> str:
    osname = platform.system()
    return raw + ".exe" if osname == "Windows" else raw

class Toolchain:
    def __init__(self, prerequisites: list, working_dir: str):
        self.prerequisites = prerequisites
        self.working_dir = working_dir
        self.valid = False

toolchains = {
    "Rust": Toolchain([
        "cargo"
    ], "Corium/Compiler/"),
    "CMake": Toolchain([
        "cmake"
    ], None),
    "GNU": Toolchain([
        "gcc-11",
        "g++-11",
    ], None),
    "LLVM": Toolchain([
        "clang",
        "clang++",
    ], None),
    "LLVM-CL": Toolchain([
        "clang-cl",
        "ninja"
    ], None)
}

build_dir = "_Build_"
out_dir_base = "Bin"
out_dir = out_dir_base

# BEGIN SETUP #
# Show description:
info_path = "Tools/ProductionBuildInfo.txt"

print("Production build generator Copyright 2021 Mario Sieg \"pinsrq\" <mt3000@gmx.de>")
print("Info path: " + info_path)

if not os.path.isfile(info_path):
    print(f"Could not find info file: {info_path}! Maybe running in wrong path? Should be at the root of the Corium dir!")

with open(info_path, "r") as infofile:
    description = infofile.read()
    print(description)

print("Beginning setup...")

osname = platform.system()
wk_dir = os.getcwd()
print("Running on: " + osname)
print("Working directory: " + str(wk_dir))
print("Build dir: " + build_dir)
print("Base output dir: " + out_dir)
print("OS suffix: " + osname.capitalize())
out_dir = f"{out_dir}/{osname.capitalize()}"
print("Final out dir: " + out_dir)

print("Checking if the correct prerequisites are installed and inside $PATH...\n")

# Check prerequisites:
for name, toolchain in toolchains.items():
    print("Checking toolchain " + name)
    toolchain.valid = True
    for pre in toolchain.prerequisites:
        is_installed = is_shell_tool_installed(pre)
        status = "OK" if is_installed else "MISSING"
        print(f"    Checking prerequisite {pre}... {status}")
        if not is_installed:
            toolchain.valid = False
    if toolchain.working_dir is not None:
        print(f"Checking working directory: {toolchain.working_dir}...")
        if not os.path.isdir(toolchain.working_dir):
            print("Working directory not found!")
            toolchain.valid = False
    status = "YES" if toolchain.valid else "NO"
    print(f"Toolchain {name} valid: {status}\n")

is_missing_toolchain = False

# Check if we have the correct toolchains
if not toolchains["Rust"].valid:
    print("Missing Rust (Cargo) toolchain! Please install Rust first and add it to $PATH!")
    is_missing_toolchain = True

if not toolchains["CMake"].valid:
    print("Missing CMAKE toolchain! Please install CMAKE first and add it to $PATH!")
    is_missing_toolchain = True

if not toolchains["GNU"].valid and not toolchains["LLVM"].valid and toolchains["LLVM-CL"].valid:
    print("Missing C++ toolchains! Please install the GNU C++ or LLVM C++ toolchain first and add it to $PATH!")
    is_missing_toolchain = True

if is_missing_toolchain:
    print("Exiting...")
    exit(-1)

print("Great, required toolchains and prerequisites are installed!")

