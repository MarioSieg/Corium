#ifdef __WIN__
#define AR0 rcx
#else
#define AR0 rdi
#endif

# extern "C" NOX_ASM_ROUTINE auto _QueryRegSet_GPR(GPRRegister64Layout* out) -> void;
# Query general purpose registers (%rax - %r15). (%rip) does not count as GPR!
SUBROUTINE(QueryRegSet_GPR):
    movq    %rax,   (%AR0)
    movq    %rbx,   8*1(%AR0)
    movq    %rcx,   8*2(%AR0)
    movq    %rdx,   8*3(%AR0)
    movq    %rsi,   8*4(%AR0)
    movq    %rdi,   8*5(%AR0)
    movq    %rsp,   8*6(%AR0)
    movq    %rbp,   8*7(%AR0)
    movq    %r8,    8*8(%AR0)
    movq    %r9,    8*9(%AR0)
    movq    %r10,   8*10(%AR0)
    movq    %r11,   8*11(%AR0)
    movq    %r12,   8*12(%AR0)
    movq    %r13,   8*13(%AR0)
    movq    %r14,   8*14(%AR0)
    movq    %r15,   8*15(%AR0)
    retq

# extern "C" NOX_ASM_ROUTINE auto _QueryRegSet_SSE(SSERegister128Layout* out) -> void;
# Query all SSE registers (%xmm0 - %xmm15)
SUBROUTINE(QueryRegSet_SSE):
    movdqu  %xmm0,  (%AR0)
    movdqu  %xmm1,  16*1(%AR0)
    movdqu  %xmm2,  16*2(%AR0)
    movdqu  %xmm3,  16*3(%AR0)
    movdqu  %xmm4,  16*4(%AR0)
    movdqu  %xmm5,  16*5(%AR0)
    movdqu  %xmm6,  16*6(%AR0)
    movdqu  %xmm7,  16*7(%AR0)
    movdqu  %xmm8,  16*8(%AR0)
    movdqu  %xmm9,  16*9(%AR0)
    movdqu  %xmm10, 16*10(%AR0)
    movdqu  %xmm11, 16*11(%AR0)
    movdqu  %xmm12, 16*12(%AR0)
    movdqu  %xmm13, 16*13(%AR0)
    movdqu  %xmm14, 16*14(%AR0)
    movdqu  %xmm15, 16*15(%AR0)
    retq

