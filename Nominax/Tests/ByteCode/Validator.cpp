TEST(ValidatorAlgorithms, FullValidation1Million)
{
	constexpr std::uint64_t count {200'000};

	Stream stream { };
	stream.Reserve(count * 5 + 10);
	stream.Prologue();

	for (std::uint64_t i {0}; i < count; ++i)
	{
		stream.Emit(Instruction::JMP);
		stream.Emit(JumpAddress {0});
		stream.Emit(Instruction::STO);
		stream.Emit(static_cast<MemOffset>(1));
		stream.Emit(-0.5);
	}

	stream.Epilogue();

	auto result {ValidationResultCode::Ok};
	const auto exec
    {
		[&result, &stream]()
		{
			result = ValidateFullPass(stream);
		}
	};

	ASSERT_NO_FATAL_FAILURE(exec());
	ASSERT_EQ(result, ValidationResultCode::Ok);
}
